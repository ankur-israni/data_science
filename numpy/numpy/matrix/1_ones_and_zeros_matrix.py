import numpy as np

# Create a matrix with all 1's - 2X3 matrix = 2 rows, 3 columns - int is the datatype
ones_matrix = np.ones((2,3),int)
print("ones_matrix:\n", ones_matrix)

# Create a matrix with all 0's - 3X3 matrix = 3 rows, 3 columns - int is the datatype
zeroes_matrix = np.zeros((3,3),int)
print("zeroes_matrix:\n", zeroes_matrix)