# Multiline string with double quotes
# in the result, the line breaks are inserted at the same position as in the code.
str1 = """Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua."""

# Multiline string with single quotes
# in the result, the line breaks are inserted at the same position as in the code.
str2 = '''Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua.'''


print(str1)
print(str2)
