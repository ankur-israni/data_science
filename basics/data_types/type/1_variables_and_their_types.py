# Print all built in datatypes in python
# str, bool, complex, int are all classes in python
# type() function gives the datatype of a particular variable

str ='hello world';
bool = True;
complex = 3 + 4j;
int = 10;
float = 10.00

list = [1,2,3]
tuple=(4,5,6)
set={7,8,9}
dictionary={1:'one',2:'two',3:'three'}


print("type of str = ",type(str));
print("type of bool = ",type(bool));
print("type of complex = ",type(complex));
print("type of int = ",type(int));
print("type of float = ",type(float));


print("type of list = ",type(list));
print("type of tuple = ",type(tuple));
print("type of set = ",type(set));
print("type of dictionary = ",type(dictionary));






