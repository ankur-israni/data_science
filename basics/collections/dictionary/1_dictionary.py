# Dictionary
# - Ordered collection
# - has key value pairs
# - Duplicates of keys is not allowed, duplicate of values is allowed
# - Mutable - can be changed - can add, remove, update key value pairs
# - Indexable - By Key

# Create a simple dictionary
dict = {'k1':'v1','k2':'v2'}

# print all the key value pairs of the dictionary
for key, value in dict:
    print(key, value);