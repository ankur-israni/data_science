# Tuple 
# Ordered collection, 
# Immutable (New elements cannot be added or deleted, but existing element values can be changed)
# Indexable (By Position)
# Duplicates allowed

# Create a simple tuple
tuple = (8,9,10)
print("tuple = ",tuple)

# Print the first element
print("First element in the tuple ",tuple[0])

# Immutable - Trying to change the first element will throw an error
# tuple[0] = 5  #--- This will throw an error



